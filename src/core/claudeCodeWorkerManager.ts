import { execFile as execFileCb } from 'child_process'
import { promisify } from 'util'
import { randomUUID } from 'crypto'

const execFile = promisify(execFileCb)

export interface TmuxResult {
  stdout: string
  stderr: string
}

export type TmuxRunner = (args: string[]) => Promise<TmuxResult>

export interface ClaudeWorkerStartOptions {
  session: string
  cwd: string
  command?: string
}

export interface ClaudeWorkerTaskOptions extends ClaudeWorkerStartOptions {
  task: string
  instructions?: string
  /**
   * P0-5: unique task id binding the completion sentinel to THIS
   * invocation. If omitted, runTask() generates one via crypto.randomUUID()
   * and returns it so the caller can pass it to waitFor(). The prompt
   * emits `[TASK_START <id>]` at the head and `[TASK_DONE <id>]` at the
   * tail; waitFor() matches only the line tagged with this id, so a
   * reused tmux session cannot cause a stale `[DONE]` from a previous
   * task to be misread as completion of the current one.
   */
  taskId?: string
}

export interface ClaudeWorkerRunTaskResult {
  session: string
  created: boolean
  syncedEnv: string[]
  /** The taskId used for this run (caller-provided or generated). */
  taskId: string
}

export interface ClaudeWorkerWaitOptions {
  session: string
  pattern?: string
  timeoutMs?: number
  intervalMs?: number
  lines?: number
  signal?: AbortSignal
  /**
   * P0-5: task id whose `[TASK_DONE <id>]` sentinel we are waiting
   * for. When supplied, waitFor() ignores any prior `[DONE]` or
   * `[TASK_DONE ...]` lines belonging to other tasks (or to previous
   * runs on a reused session). If both `pattern` and `taskId` are
   * supplied, `taskId` wins.
   */
  taskId?: string
}

export interface ClaudeWorkerWaitResult {
  matched: boolean
  output: string
  aborted?: boolean
  /** 'done' for success sentinel, 'failed' for TASK_FAILED sentinel. */
  matchKind?: 'done' | 'failed'
  /** Extracted reason from a [TASK_FAILED <id> reason=...] match. */
  failureReason?: string
}

const DEFAULT_CLAUDE_COMMAND = 'claude'
const DEFAULT_DONE_PATTERN = '^\\[DONE\\]$'
const CLAUDE_ENV_KEYS = [
  'ANTHROPIC_AUTH_TOKEN',
  'ANTHROPIC_API_KEY',
  'ANTHROPIC_BASE_URL',
  'ANTHROPIC_MODEL',
  'ANTHROPIC_DEFAULT_SONNET_MODEL',
  'ANTHROPIC_DEFAULT_OPUS_MODEL',
  'ANTHROPIC_DEFAULT_HAIKU_MODEL',
  'CLAUDE_CODE_DISABLE_NONESSENTIAL_TRAFFIC',
]

function collectClaudeEnvironment(env: NodeJS.ProcessEnv): Array<[string, string]> {
  const entries: Array<[string, string]> = []
  for (const key of CLAUDE_ENV_KEYS) {
    const value = env[key]
    if (!value) continue
    if (value.includes('\n')) {
      throw new Error(`Refusing to sync multiline environment variable: ${key}`)
    }
    entries.push([key, value])
  }
  return entries
}

export function claudeWorkerSessionName(name: string): string {
  const clean = name
    .trim()
    .replace(/[^a-zA-Z0-9_.-]/g, '-')
    .replace(/^-+|-+$/g, '')
  return clean || `ovogo-claude-${Date.now()}`
}

/**
 * P0-5: build the Claude Code worker prompt bound to a unique task id.
 *
 * Pre-fix: the prompt emitted a static `[DONE]` sentinel and waitFor()
 * matched `^\[DONE\]$`. On a reused tmux session the pane still held
 * the previous task's `[DONE]` line — the first poll iteration matched
 * it and waitFor() returned `{matched:true}` BEFORE the worker had
 * processed the new task. The supervisor trusted a success that had
 * never happened.
 *
 * Post-fix: the prompt emits `[TASK_START <id>]` at the head and
 * `[TASK_DONE <id>]` at the tail. waitFor(taskId) matches ONLY the
 * line tagged with that id, so a stale `[TASK_DONE <other-id>]` from
 * history cannot satisfy the current wait.
 *
 * The id is generated by the caller (or by runTask() via
 * crypto.randomUUID() if not supplied) and round-trips through
 * (prompt → worker output → waitFor match).
 */
export function buildClaudeWorkerPrompt(task: string, instructions?: string, taskId?: string): string {
  const id = taskId ?? generateTaskId()
  return [
    `[TASK_START ${id}]`,
    '',
    'You are a Claude Code worker controlled by a supervisor agent.',
    'Follow the task exactly. Keep changes narrowly scoped.',
    'Do not commit. Do not push. Do not modify unrelated files.',
    'When complete, print the final block below EXACTLY (preserving the id).',
    'If the task FAILED and cannot be completed, print the failure block instead.',
    '',
    '[TASK]',
    task,
    '',
    instructions ? '[INSTRUCTIONS]\n' + instructions + '\n' : '',
    '[FINAL OUTPUT FORMAT]',
    `[TASK_DONE ${id}]`,
    'Summary:',
    'Files:',
    'Tests:',
    '',
    '[OR — IF THE TASK FAILED]',
    `[TASK_FAILED ${id} reason=<short description>]`,
  ].filter(Boolean).join('\n')
}

/** Generate a unique task id for prompt↔waitFor binding (P0-5). */
export function generateTaskId(): string {
  return randomUUID()
}

/**
 * P0-5: build the regex that matches ONLY the `[TASK_DONE <id>]` line
 * for the given task id. Anchored to the start of a line so the
 * worker's prose summary cannot accidentally contain a substring
 * that satisfies the match.
 */
export function taskDonePattern(taskId: string): string {
  const escaped = taskId.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
  return `^\\[TASK_DONE ${escaped}\\]$`
}

export function taskFailedPattern(taskId: string): string {
  const escaped = taskId.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
  return `^\\[TASK_FAILED ${escaped} reason=(.+?)\\]$`
}

function compilePattern(pattern: string): RegExp {
  try {
    return new RegExp(pattern, 'm')
  } catch {
    throw new Error('Invalid regex pattern: ' + pattern)
  }
}

function delay(ms: number, signal?: AbortSignal): Promise<'done' | 'aborted'> {
  if (signal?.aborted) return Promise.resolve('aborted')
  return new Promise((resolve) => {
    const timer = setTimeout(() => {
      signal?.removeEventListener('abort', onAbort)
      resolve('done')
    }, ms)
    const onAbort = () => {
      clearTimeout(timer)
      resolve('aborted')
    }
    signal?.addEventListener('abort', onAbort, { once: true })
  })
}

async function defaultTmuxRunner(args: string[]): Promise<TmuxResult> {
  try {
    const { stdout, stderr } = await execFile('tmux', args, {
      encoding: 'utf8',
      maxBuffer: 1024 * 1024,
    })
    return { stdout, stderr }
  } catch (error: unknown) {
    const err = error as { stdout?: string; stderr?: string; message?: string }
    const message = [err.stdout, err.stderr, err.message].filter(Boolean).join('\n').trim()
    throw new Error(message || String(error), { cause: error })
  }
}

export class ClaudeCodeWorkerManager {
  /**
   * P0-9: sessions created by THIS manager instance. Used by
   * dispose() to reclaim tmux sessions on engine shutdown — without
   * this, calling Claude Code via tmux leaks a session per task
   * until the host process exits (or the user notices and kills
   * them by hand). Sessions created externally (by other processes,
   * or pre-existing on the host) are NOT tracked here — we only
   * reclaim what we created.
   */
  private readonly createdSessions = new Set<string>()

  constructor(private readonly runner: TmuxRunner = defaultTmuxRunner) {}

  async syncClaudeEnvironment(session: string, env: NodeJS.ProcessEnv = process.env): Promise<string[]> {
    const entries = collectClaudeEnvironment(env)
    for (const [key, value] of entries) {
      await this.runner(['set-environment', '-t', session, key, value])
    }
    return entries.map(([key]) => key)
  }

  async sessionExists(session: string): Promise<boolean> {
    try {
      await this.runner(['has-session', '-t', session])
      return true
    } catch {
      return false
    }
  }

  async start(options: ClaudeWorkerStartOptions): Promise<{ session: string; created: boolean; syncedEnv: string[] }> {
    const session = claudeWorkerSessionName(options.session)
    const envEntries = collectClaudeEnvironment(process.env)
    const syncedEnv = envEntries.map(([key]) => key)
    if (await this.sessionExists(session)) {
      await this.syncClaudeEnvironment(session)
      // P0-9: track this session even when reused so dispose()
      // reclaims it on shutdown (the host created/reused it).
      this.createdSessions.add(session)
      return { session, created: false, syncedEnv }
    }

    await this.runner([
      'new-session',
      '-d',
      '-s',
      session,
      '-c',
      options.cwd,
      ...envEntries.flatMap(([key, value]) => ['-e', `${key}=${value}`]),
      options.command ?? DEFAULT_CLAUDE_COMMAND,
    ])
    // P0-9: track the session we just created.
    this.createdSessions.add(session)
    return { session, created: true, syncedEnv }
  }

  async send(session: string, text: string): Promise<void> {
    if (!text || !text.trim()) {
      throw new Error('Cannot send empty text to worker')
    }
    const buffer = `ovogo-${Date.now()}-${Math.random().toString(16).slice(2)}`
    await this.runner(['set-buffer', '-b', buffer, text])
    try {
      await this.runner(['paste-buffer', '-t', session, '-b', buffer])
      await this.runner(['send-keys', '-t', session, 'Enter'])
      // Claude Code's terminal editor may keep the pasted text staged after the
      // first Enter. A second Enter reliably submits while a blank follow-up is
      // ignored by the idle prompt.
      await this.runner(['send-keys', '-t', session, 'Enter'])
    } finally {
      try {
        await this.runner(['delete-buffer', '-b', buffer])
      } catch {
        // best-effort cleanup; stale buffers are harmless and small
      }
    }
  }

  async runTask(options: ClaudeWorkerTaskOptions): Promise<ClaudeWorkerRunTaskResult> {
    // P0-5: bind every run to a unique task id so waitFor() can
    // distinguish THIS run's [TASK_DONE <id>] from any stale [DONE]
    // or [TASK_DONE <other-id>] left over in the pane from prior runs.
    const taskId = options.taskId ?? generateTaskId()
    const started = await this.start(options)
    await this.send(started.session, buildClaudeWorkerPrompt(options.task, options.instructions, taskId))
    return { ...started, taskId }
  }

  async capture(session: string, lines = 80): Promise<string> {
    const safeLines = Number.isFinite(lines) ? lines : 80
    const start = safeLines <= 0 ? '-' : `-${Math.max(1, Math.floor(safeLines))}`
    const { stdout } = await this.runner(['capture-pane', '-t', session, '-p', '-S', start])
    return stdout.trim()
  }

  async waitFor(options: ClaudeWorkerWaitOptions): Promise<ClaudeWorkerWaitResult> {
    const pattern = options.pattern
      ?? (options.taskId ? taskDonePattern(options.taskId) : DEFAULT_DONE_PATTERN)
    const failPattern = options.taskId && !options.pattern
      ? taskFailedPattern(options.taskId)
      : null
    const timeoutMs = Math.max(1, options.timeoutMs ?? 120_000)
    const intervalMs = Math.max(100, options.intervalMs ?? 2_000)
    const deadline = Date.now() + timeoutMs
    const regex = compilePattern(pattern)
    const failRegex = failPattern ? compilePattern(failPattern) : null
    let output = ''

    while (Date.now() <= deadline) {
      if (options.signal?.aborted) return { matched: false, output, aborted: true }
      output = await this.capture(options.session, options.lines ?? 120)
      if (failRegex) {
        const failMatch = output.match(failRegex)
        if (failMatch) {
          return { matched: true, output, matchKind: 'failed', failureReason: failMatch[1] }
        }
      }
      if (regex.test(output)) return { matched: true, output, matchKind: 'done' }
      const remainingMs = deadline - Date.now()
      if (remainingMs <= 0) break
      const state = await delay(Math.min(intervalMs, remainingMs), options.signal)
      if (state === 'aborted') return { matched: false, output, aborted: true }
    }

    return { matched: false, output }
  }

  async list(): Promise<string[]> {
    try {
      const { stdout } = await this.runner(['list-sessions', '-F', '#S'])
      return stdout.split('\n').map((line) => line.trim()).filter(Boolean)
    } catch {
      return []
    }
  }

  /** Like `list()` but throws when tmux itself is unreachable. */
  async listOrThrow(): Promise<string[]> {
    const { stdout } = await this.runner(['list-sessions', '-F', '#S'])
    return stdout.split('\n').map((line) => line.trim()).filter(Boolean)
  }

  /**
   * Kill a tmux session.  Idempotent — returns `{ stopped: false }` when the
   * session is already gone so callers don't need a pre-check.
   */
  async stop(session: string): Promise<{ stopped: boolean }> {
    if (!await this.sessionExists(session)) return { stopped: false }
    await this.runner(['kill-session', '-t', session])
    // P0-9: drop from tracked set so dispose() doesn't try to kill it again.
    this.createdSessions.delete(session)
    return { stopped: true }
  }

  /**
   * P0-9: stop every session this manager instance created (or reused).
   * Called by engine.dispose() so tmux worker sessions do not outlive
   * the runtime. Best-effort: individual failures are swallowed so a
   * single dead session doesn't prevent the rest from being cleaned
   * up. Returns the count of successfully-stopped sessions so callers
   * can log the result.
   *
   * NOTE: sessions created by other host processes (or pre-existing
   * on the machine) are NOT touched — we only reclaim what this
   * manager started.
   */
  async dispose(): Promise<{ stopped: number; failed: number }> {
    const sessions = Array.from(this.createdSessions)
    this.createdSessions.clear()
    let stopped = 0
    let failed = 0
    for (const session of sessions) {
      try {
        if (await this.sessionExists(session)) {
          await this.runner(['kill-session', '-t', session])
        }
        stopped++
      } catch {
        failed++
      }
    }
    return { stopped, failed }
  }
}
